     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $events = Event::where('start', date('Y-m-d'))->get();

        foreach($events as $event) {
            if ($event->type === 'ship') {
                $schedule = Shipschedule::find($event->type_id);
            } else {
                $schedule = Vehicleschedule::find($event->type_id);
            }

            $schedule->status = 0;
            $schedule->save();
        }

        $data = array('message'=>'success','code'=> 200);
        return response()->json($data, 200);
    }

